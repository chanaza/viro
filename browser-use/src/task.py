from src.config import SEARCH_TARGET, BUSINESS_PROFILE_PLATFORMS, TRUSTED_AGGREGATORS


def build_navigation_rules() -> str:
    return """
    **כללי ניווט באתרים — זהה את סוג הרכיב וקבע כיצד לפעול:**
    1. **Combobox / dropdown** — כל הפריטים בזיכרון ה-DOM מרגע הפתיחה: חלץ מיד וסיים.
    2. **רשימה עם כפתור "טען עוד" / "הצג עוד תוצאות"** — לחץ על הכפתור בלולאה עד שנעלם, ואז חלץ.
    3. **גלילה אינסופית** — פריטים נטענים תוך כדי גלילה: גלול למטה בלולאה עד שאין פריטים חדשים, ואז חלץ.
    4. **רשימה רנדרית רגילה** (תוצאות חיפוש, כרטיסים שכבר נטענו) — חלץ ישירות את מה שקיים ב-DOM.
    בכל סוגי ה-pagination עם דפים (כפתור "הבא" / מספרי דפים): עבור לפי סדר עולה עד הסוף, אל תדלג ואל תחזור אחורה.

    **מעבר בין מקורות:** כשמקור נכשל או חסום — נווט ישירות למקור הבא בטאב הנוכחי (new_tab: False). אל תסגור טאבים ותפתח חדשים — זה עלול לגרום לקריסת הדפדפן.

    **חילוץ נתונים מדף:** השתמש ב-extract לחילוץ נתונים מובנים. אם extract החזיר תוצאה ריקה או לא רלוונטית — נסה evaluate פעם אחת בלבד. אם גם evaluate נכשל — עבור למקור הבא, אל תיכנס ללופ של ניסיונות חוזרים.
    """


def build_task(subject: str) -> str:
    platforms = ", ".join(f"{p['name']} ({p['domain']})" for p in BUSINESS_PROFILE_PLATFORMS)

    aggregator_lines = ""
    for agg in TRUSTED_AGGREGATORS:
        line = f"   - {agg['domain']}"
        if agg["instructions"]:
            line += f": {agg['instructions']}"
        aggregator_lines += line + "\n"

    return f"""
    אתה סוכן מחקר אוטומטי. מטרתך: מצא את כל ה{SEARCH_TARGET} של רשת "{subject}" בישראל — שמות, כתובות, ערים — ממקורות אינטרנט.

    **כלל כללי:** אם מקור כלשהו סגור, חסום, מוגן בסיסמה, או שלא ניתן לתפקד בו מבחינה טכנית — עבור מיד למקור הבא.

    **איסוף נתונים:** אל תיצור קבצי todo.md, תכנון, או מעקב. אם ניסית ליצור קובץ ונכשלת — התעלם מהכשלון והמשך ישירות בביצוע המשימה.
    כאשר מצאת רכיב (dropdown/combobox) המכיל את רשימת ה{SEARCH_TARGET} המלאה — קרא ל-done() מיד באותו שלב עם כל הנתונים שקראת, לפני שממשיכים לפעולה אחרת.

    **סיום:** בסיום האיסוף — קרא ל-done() ישירות עם כל הנתונים שאספת. אל תשמור נתונים לקבצים תוך כדי הריצה.

    {build_navigation_rules()}

    **סדר עדיפויות למקורות — הסדר מחייב. יש לנסות כל מקור לפי הסדר המדויק ואין לדלג על מקורות:**
    1. חפש בגוגל: '"{subject}" {SEARCH_TARGET}' — נווט לתוצאה הראשונה, בתנאי שהיא שייכת לאתר הרשמי של הרשת.
       אם יש אפשרות "כל הארץ" / "הצג הכל" — השתמש בה ואל תסנן לפי עיר.
       עבור מיד לשלב הבא אם מתקיים אחד מאלה:
         - הדומיין של הדף שנפתח אינו שייך לרשת עצמה (כלומר: אינו האתר הרשמי של "{subject}")
         - האתר מחייב בחירת עיר ספציפית ואין אפשרות לרשימה ארצית
         - נפתח modal/popup וניסית לאינטראקציה איתו פעמיים ועדיין אין רשימת {SEARCH_TARGET}
         - ניסית לנווט לדף {SEARCH_TARGET} יותר מפעמיים ולא הגעת לרשימה
    2. חפש בגוגל: '"{subject}" אתר שקיפות מחירים' — נסה לגשת לאתר שנמצא.
       אם החיפוש מוביל לפורטל ממשלתי ולא לאתר ייעודי של הרשת — נווט לפורטל, חפש בו את שם הרשת (או שם קרוב) ולחץ על הקישור לאתר שקיפות המחירים הייעודי שלה.
       אתרי שקיפות מחירים מפרסמים קבצי מחירים לפי סניף — יכולות להיות מספר רשומות לאותו סניף.
       בהכרח מופיעים בו שמות הסניפים: בטבלת הקבצים, בתפריט בחירת סניף (combobox), בחיפוש, או בכל מקום אחר.
       אם כל מה שמצאת הוא קודים מספריים (כגון "202", "Branch 202") ואין שמות עבריים של סניפים — עבור למקור הבא.
       בסוף — הגש רשימה ייחודית של כל הסניפים שמצאת (ללא כפילויות).
    3. הדף העסקי הרשמי של הרשת — גלוש לכל אחת מהפלטפורמות הבאות אחת אחת לפי הסדר: {platforms}.
       לכל פלטפורמה: נווט ישירות לדומיין שלה, חפש את הדף הרשמי של הרשת, נווט אליו, וחפש בו את רשימת ה{SEARCH_TARGET}.
       אם פלטפורמה חסומה — עבור מיד לפלטפורמה הבאה.
    4. אתרי אגרגטור — גלוש ישירות לכל אחד לפי כתובתו ברשימה זו, לפי הסדר:
{aggregator_lines}
    5. חיפוש אחד בגוגל: '"{subject}" {SEARCH_TARGET}' — בחן את התוצאות הרלוונטיות בעמוד הראשון בלבד. אל תמשיך לעמוד הבא ואל תבצע חיפוש נוסף.

    **כלל עצירה:** לאחר סיום מלא של כל מקור — בדוק אם הרשימה שאספת בפועל מכסה ערים מרחבי הארץ (צפון, מרכז, דרום).
    אם כן — עצור ואל תמשיך למקור הבא (גם אם הכתובות לא הושלמו במלואן).
    אם לא (מקור לא נגיש, או כיסוי גיאוגרפי חלקי בלבד) — המשך למקור הבא.
    הערה: כתובת חלקית (כגון שם הרחוב בתוך שם הסניף) מספיקה — אין צורך להשלים כתובות ממקורות נוספים.

    עבור כל פריט חלץ: name (שם הסניף), address (כתובת — ריק אם לא נמצא), city (עיר — ריק אם לא נמצא), source (שם האתר).
    """
