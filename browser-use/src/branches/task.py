from src.branches.config import BUSINESS_PROFILE_PLATFORMS, TRUSTED_AGGREGATORS


def get_branches_specific_instructions(subject: str) -> str:
    """Returns specific_instructions for the branches research task."""
    platforms = ", ".join(f"{p['name']} ({p['domain']})" for p in BUSINESS_PROFILE_PLATFORMS)

    aggregator_lines = ""
    for agg in TRUSTED_AGGREGATORS:
        line = f"   - {agg['domain']}"
        if agg["instructions"]:
            line += f": {agg['instructions']}"
        aggregator_lines += line + "\n"

    specific_instructions = f"""
    **סדר עדיפויות — 5 שלבים. כל שלב עשוי לכלול אתר אחד או מספר אתרים. הסדר מחייב — אין לדלג על שלב או אתר:**
    1. **שלב 1 — האתר הרשמי:** חפש בגוגל: '"{subject}" סניפים' — נווט לתוצאה הראשונה, בתנאי שהיא שייכת לאתר הרשמי של הרשת.
       אם יש אפשרות "כל הארץ" / "הצג הכל" — השתמש בה ואל תסנן לפי עיר.
       עבור מיד לשלב 2 אם מתקיים אחד מאלה:
         - הדומיין של הדף שנפתח אינו שייך לרשת עצמה (כלומר: אינו האתר הרשמי של "{subject}")
         - האתר מחייב בחירת עיר ספציפית ואין אפשרות לרשימה ארצית
         - נפתח modal/popup וניסית לאינטראקציה איתו פעמיים ועדיין אין רשימת סניפים
         - ניסית לנווט לדף סניפים יותר מפעמיים ולא הגעת לרשימה
    2. **שלב 2 — שקיפות מחירים:** חפש בגוגל: '"{subject}" אתר שקיפות מחירים' — נסה לגשת לאתר שנמצא.
       אם החיפוש מוביל לפורטל ממשלתי ולא לאתר ייעודי של הרשת — נווט לפורטל, חפש בו את שם הרשת (או שם קרוב) ולחץ על הקישור לאתר שקיפות המחירים הייעודי שלה.
       אתרי שקיפות מחירים מפרסמים קבצי מחירים לפי סניף — יכולות להיות מספר רשומות לאותו סניף.
       בהכרח מופיעים בו שמות הסניפים: בטבלת הקבצים, בתפריט בחירת סניף (combobox), בחיפוש, או בכל מקום אחר.
       אם כל מה שמצאת הוא קודים מספריים (כגון "202", "Branch 202") ואין שמות עבריים של סניפים — עבור לשלב 3.
       בסוף — הגש רשימה ייחודית של כל הסניפים שמצאת (ללא כפילויות).
    3. **שלב 3 — פלטפורמות עסקיות:** שלב זה כולל את האתרים הבאים לפי הסדר: {platforms}.
       לכל אתר: נווט ישירות לדומיין שלו, חפש את הדף הרשמי של הרשת, נווט אליו, וחפש בו את רשימת הסניפים.
       אם אתר חסום — עבור מיד לאתר הבא בשלב זה.
    4. **שלב 4 — אגרגטורים:** שלב זה כולל את האתרים הבאים לפי הסדר:
{aggregator_lines}
    5. **שלב 5 — חיפוש גוגל:** חיפוש אחד בגוגל: '"{subject}" סניפים' — בחן את התוצאות הרלוונטיות בעמוד הראשון בלבד. אל תמשיך לעמוד הבא ואל תבצע חיפוש נוסף.

    הערה: כתובת חלקית (כגון שם הרחוב בתוך שם הסניף) מספיקה — אין צורך להשלים כתובות מאתרים נוספים.

    עבור כל פריט חלץ: name (שם הסניף), address (כתובת — ריק אם לא נמצא), city (עיר — ריק אם לא נמצא), source (שם האתר).
    בלוג: source = הדומיין של האתר (למשל: "google.com", "easy.co.il", "maps.google.com").
    """

    return specific_instructions
